// Loops - use to repeatedly execute a block of condition until a specific condition is way. It is a way to automate repetitive.

//1. For loop
for (let i = 1; i <= 5; i++) {
    console.log("For loop count:", i);
}

//2. While loop
let count = 1;
while (count <= 5) {
    console.log("While loop count:", count);
    count++;
}

//3. Do-while loop
let x = 10;
do {
    console.log("Do while count:", x)
    x++;
} while  (x <= 5);